def build_question_bank(topic_definitions, patterns):
    bank = {}
    for topic_slug, entries in topic_definitions.items():
        questions = []
        for entry in entries:
            for question_template, answer_template in patterns:
                questions.append(
                    (
                        question_template.format(**entry),
                        answer_template.format(**entry),
                    )
                )
        if len(questions) != QUESTION_COUNT_PER_TOPIC:
            raise ValueError(
                f"{topic_slug} produced {len(questions)} questions instead of {QUESTION_COUNT_PER_TOPIC}"
            )
        bank[topic_slug] = questions
    return bank


